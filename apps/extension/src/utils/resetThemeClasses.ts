export function resetThemeClasses() {
    document.documentElement.classList.remove(
        "light",
        "dark",

        "gray",
        "blue",
        "red",
        "green",
        "violet",
        "orange",
        "rose",
        "yellow",

        "lightblue",
        "lightgray",
        "lightred",
        "lightgreen",
        "lightviolet",
        "lightorange",
        "lightrose",
        "lightyellow",

        "darkblue",
        "darkgray",
        "darkred",
        "darkgreen",
        "darkviolet",
        "darkorange",
        "darkrose",
        "darkyellow"
    );
}
